num1=float(input("enter the number"))
num2=float(input("enter the number"))
op = input("enter operation")
if op == '+':
   print("result=",num1+num2)
elif op == "-":
   print("result=",num1-num2)
elif op == "*":
   print("result=",num1*num2)
elif op == "/":
   print("result=",num1/num2)
else:
   print("invalid")
